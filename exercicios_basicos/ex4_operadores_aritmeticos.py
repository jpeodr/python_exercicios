# Operadores aritméticos

a = 10
b = 3
x = a + b       # Soma
y = a - b       # Subtração
z = a * b       # Multiplicação
w = a / b       # Divisão
r = a % b       # Resto da divisão
t = a ** b      # Potência
u = a // b      # Quociente da divisão

print("Soma: ", x)
print("Subtração: ", y)
print("Multiplicação: ", z)
print("Divisão: ", w)
print("Resto da divisão: ", r)
print("Potência: ", t)
print("Quociente da divisão: ", u)
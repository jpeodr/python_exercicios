# Exemplos de operadores relacionais

a = 10
b = 5
x = a == b      # Igual
y = a != b      # Diferente
z = a > b       # Maior que
w = a < b       # Menor que
t = a >= b      # Maior ou igual a
u = a <= b      # Menor ou igual a

print(f"{a} é igual a {b}?  ", x)
print(f"{a} é diferente de {b}?  ", y)
print(f"{a} é maior que {b}?  ", z)
print(f"{a} é menor que {b}?  ", w)
print(f"{a} é maior ou igual a {b}?  ", t)
print(f"{a} é menor ou igual a {b}?  ", u)